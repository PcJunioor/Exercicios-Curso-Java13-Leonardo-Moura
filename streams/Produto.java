package streams;

public class Produto {
	
	final String nome;
	final double preco;
	final double frete;
	final double desconto;
	
	
	public Produto(String nome, double preco, double frete, double desconto) {
		
		this.nome = nome;
		this.preco = preco;
		this.frete = frete;
		this.desconto = desconto;
	}
	
	
	
	

}
